		global $translations;

		$lang = isset( $_GET['lang'] ) && $_GET['lang'] ? $_GET['lang'] : 'en';

		if ( isset( $translations[$key][$lang] ) && $translations[$key][$lang] ) {
			return $translations[$key][$lang];
		} else {
			return $translations[$key]['en'];
		}
	}
